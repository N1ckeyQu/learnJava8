package com.learn;

public class TriAngle {
    private int base;
    private int height;

    public TriAngle(int b, int h) {
        base = b;
        height = h;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
